// Final drug list = core drugs + any JSON/CSV imports, with exam tags applied.
// Import path: drop objects into data/drugs.json (see data/README.md).
export const DRUGS: Drug[] = [...CORE_DRUGS, ...(extraDrugs as Drug[])].map((d) => ({
  ...d,
  importantForExam: d.importantForExam ?? IMPORTANT_FOR_EXAM.has(d.id),
  askedFrequently: d.askedFrequently ?? ASKED_FREQUENTLY.has(d.id),
}));

// Fail fast on duplicate drug IDs. getDrug() and the /drug/[id] route rely on
// find(), which silently returns only the first match — a duplicate id would
// hide a drug and double it in flashcards/MCQs. This throws at module load
// (build + dev), so mistakes surface immediately rather than shipping.
(function assertUniqueDrugIds(list: Drug[]) {
  const seen = new Set<string>();
  const dups = new Set<string>();
  for (const d of list) {
    if (seen.has(d.id)) dups.add(d.id);
    seen.add(d.id);
  }
  if (dups.size) {
    throw new Error(`Duplicate drug id(s) in drug data: ${[...dups].join(", ")}`);
  }
})(DRUGS);

// Validate every MCQ: exactly ≥2 options and an in-range answerIndex, so a
// mis-typed answerIndex can never point at a non-existent (or wrong) option.
// Throws at module load (build + dev) rather than silently mis-scoring a test.
(function assertValidMCQAnswers(list: Drug[]) {
  const bad: string[] = [];
  for (const d of list) {
    for (const m of d.mcqs) {
      if (m.options.length < 2 || m.answerIndex < 0 || m.answerIndex >= m.options.length) {
        bad.push(`${d.id}/${m.id} (answerIndex ${m.answerIndex}, ${m.options.length} options)`);
      }
    }
  }
  if (bad.length) {
    throw new Error(`Invalid MCQ answerIndex in drug data: ${bad.join("; ")}`);
  }
})(DRUGS);

export function getDrug(id: string): Drug | undefined {
  return DRUGS.find((d) => d.id === id);
}

export function getDrugsByTopic(topic: string): Drug[] {
  return DRUGS.filter((d) => d.topic === topic);
}

export function getImportantDrugs(): Drug[] {
  return DRUGS.filter((d) => d.importantForExam);
}

export function getFrequentDrugs(): Drug[] {
  return DRUGS.filter((d) => d.askedFrequently);
}
